DEFINE TERMINAL, $8000
DEFINE KEYBOARD, $8001

ORG $0000
  JMP .START
  JMP .INT
START:
  LDI  %SPY, $02
  LDI  %CSY, $40
  LDI  %SR,  $80
  WAIT
INT:
  LDA  KEYBOARD
  CMPI %RA,  '1'
  JNZ  .START
  CALL .PRINT_CHAR
  JMP  .START
PRINT_CHAR:
  LDI  %SPX, $00
NOT_NULL_CHAR:
  POP  %RA
  STA  TERMINAL
  CMP  %RA,  %R0
  JNZ  .NOT_NULL_CHAR
  RET
ORG $0200
DB "Hello World!", 10, $00
